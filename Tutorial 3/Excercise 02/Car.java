public class Car {
    private int numDoor;
    private String color;

    public Car(int numDoor, String color) {
        this.numDoor = numDoor;
        this.color = color;
    }

    public int getNumDoor() {
        return numDoor;
    }

    public void setNumDoor(int numDoor) {
        this.numDoor = numDoor;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
